package 예제.ch2;

public class SpecialChar {
    public static void main(String[] args) {

        char single = '\'';
        String dblQuote = "\"Hello\"";
        String root = "c:\\";

        System.out.println("single = " + single);
        System.out.println("dblQuote = " + dblQuote);
        System.out.println("root = " + root);

    }
}
